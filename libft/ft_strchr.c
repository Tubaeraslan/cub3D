#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	if (s == NULL && c)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i++;
	}
	if ((char)c == '\0')
		return ((char *)(s + i));
	return (NULL);
}
